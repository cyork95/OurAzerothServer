-- Lower Blackrock Spire (LBRS)
Travel to Blackrock Mountain: Go to [G29.0,38.0 Burning Steppes]
Navigate inside the mountain to the Spire balcony.
Speak to Vaelan to accept the UBRS key quest: Accept [QA4742 Seal of Ascension]: Go to [G29,38 Blackrock Mountain]
Enter Lower Blackrock Spire (LBRS).

Inside LBRS:
- Collect 3 Gemstones from LBRS bosses: Gem of Smolderthorn, Gem of Spirestone, Gem of Bloodaxe.
- Defeat Highlord Omokk: Target [TAR9196 Highlord Omokk]
  - Pull him to the balcony to avoid adds.
- Defeat Shadow Hunter Vosh'gajin: Target [TAR9236 Shadow Hunter Vosh'gajin]
  - Use [UI12278 Empty Vial] on Mother Smolderweb for [QC4867 Mother's Milk].
- Defeat Warmaster Voone: Target [TAR9237 Warmaster Voone]
- Defeat Urok Doomhowl (Optional Event):
  - Place Omokk's Head on the pike to summon Urok.
  - Defeat Urok Doomhowl: Target [TAR9570 Urok Doomhowl] for [QC4788].
- Defeat Overlord Wyrmthalak (LBRS End Boss): Target [TAR9568 Overlord Wyrmthalak]
  - Complete [QC4941][GA Alliance] or [QC4903][GA Horde].

-- Upper Blackrock Spire (UBRS)
Turn in [QT4742 Seal of Ascension] to Vaelan to obtain the [CI12344 Seal of Ascension] key ring.
Form a raid group and enter UBRS.

Inside UBRS:
- Defeat Pyroguard Emberstrider: Target [TAR9816 Pyroguard Emberstrider]
  - Have 3 players click the altar to start the event.
- Defeat Rend Blackhand: Target [TAR10429 Rend Blackhand]
  - Complete the Arena event waves.
- Defeat The Beast: Target [TAR10430 The Beast]
  - Rescue Finkle Einhorn from his stomach: Complete [QC5067].
- Defeat General Drakkisath (End Boss): Target [TAR10363 General Drakkisath]
  - Complete [QC5102 Drakkisath's Demise].
  - [GA Horde] Complete [QC4974 For The Horde!].

