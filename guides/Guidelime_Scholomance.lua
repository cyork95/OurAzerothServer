Kill Jandice: Target: [TAR10503 Jandice Barov].

-- Rattlegore
Kill Rattlegore in the basement to get the Viewing Room Key.

-- Kirtonos the Herald
Use the Blood of Innocents at the porch to summon him.
Kill Kirtonos: Target: [TAR10506 Kirtonos the Herald].

-- Ras Frostwhisper
Kill the Lich: Target: [TAR10508 Ras Frostwhisper].

-- Darkmaster Gandling
Kill the final boss: Target: [TAR1853 Darkmaster Gandling].

-- COMPLETION
