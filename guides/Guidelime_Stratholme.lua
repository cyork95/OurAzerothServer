-- Prerequisites & Quest Gathering
Step: Gather quests at Light's Hope Chapel, Eastern Plaguelands:
- Accept [QA5212 The Flesh Does Not Lie] from Betina Bigglezink: Go to [G75.0,52.0 Eastern Plaguelands]
- Accept [QA5213 The Active Agent] from Betina Bigglezink: Go to [G75.0,52.0 Eastern Plaguelands] (Requires completing The Flesh Does Not Lie)
- Accept [QA5263 Above and Beyond] from Duke Nicholas Zverenhoff: Go to [G75.0,52.0 Eastern Plaguelands]
- Accept [QA5243 Houses of the Holy] from Leonid Barthalomew the Revered: Go to [G75.0,52.0 Eastern Plaguelands]
- [D2 Step] Accept [QA8963 Dead Man's Plea] from Anthion Harmon (Ghost outside Service Gate): Go to [G27.0,12.0 Eastern Plaguelands]

-- Entering the Dungeon
Go to Service Gate (Undead Entrance): Go to [G27,12 Eastern Plaguelands]
- Requires the Key to the City (from Magistrate Barthilas) or someone to open it.
- Note: Aurius stands at the entrance and accepts [QT5123 The Medallion of Faith].
Enter the instance portal.

-- Inside the Instance
Step: Clear the 3 Ziggurats:
- Loot Stratholme Holy Water from supply crates throughout the dungeon for [QC5243].
- Collect Plagued Flesh Samples from ghouls/undead for [QC5212] / [QC5213].
- Go to Nerub'enkan's Ziggurat:
  - Defeat Nerub'enkan: Target [TAR10438 Nerub'enkan]
  - Enter the ziggurat and kill all Acolytes inside.
- Go to Baroness Anastari's Ziggurat:
  - Defeat Baroness Anastari: Target [TAR10436 Baroness Anastari]
  - Enter the ziggurat and kill all Acolytes inside.
- Go to Maleki the Pallid's Ziggurat:
  - Defeat Maleki the Pallid: Target [TAR10437 Maleki the Pallid]
  - Enter the ziggurat and kill all Acolytes inside.
  - Complete [QC5213] by collecting Scourge Data from the ziggurat.

Step: Slaughter Square & Baron Run:
- Defeat Magistrate Barthilas: Target [TAR10435 Magistrate Barthilas]
  - Note: Drops Key to the City.
- Proceed through the gate to Slaughter Square.
- Defeat the Abominations in the courtyard to spawn Ramstein.
- Defeat Ramstein the Gorger: Target [TAR10439 Ramstein the Gorger]
- Kill the waves of Black Guard Sentries.
- Enter Baron's lair.
- [D2 Step] **Timed Run:** Defeat Baron Rivendare within 45 minutes of entering the dungeon to save Ysida Harmon for [QC8963].
- Defeat Baron Rivendare: Target [TAR10440 Baron Rivendare]
- Loot Baron Rivendare's Head for [QC5263].

