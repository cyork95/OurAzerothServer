- Accept [QA4143 Haze of Evil] from Gregan Brewspewer in Feralas

Step: Gather Level 50 Class Quests:
- [A Warrior] Accept [QA8425 Voodoo Feathers] in your capital city
- [A Paladin] Accept [QA8418 Forging the Mightstone] in Stormwind
- [A Rogue] Accept [QA8236 The Azure Key] in Ravenholdt Manor
- [A Shaman] Accept [QA8413 Da Voodoo] in Orgrimmar
- [A Warlock] Accept [QA8422 Trolls of a Feather] in Felwood
- [A Hunter] Accept [QA8232 The Green Drake] in Azshara

-- Entering the Dungeon
Travel to Pool of Tears in Swamp of Sorrows: Go to [G69.5,53.0 Swamp of Sorrows]
Swim down and enter the Temple.
Clear the outer corridors and enter the instance portal.

-- Inside the Instance
Step: The Pit & Statue Puzzle:
- Clear the lower level (The Pit) to collect Atal'ai Haze from Oozes and Lurkers for [QC4143].
- Activate the statues in the correct circle sequence:
  1. South (Bottom)
  2. North (Top)
  3. South-East (Bottom Right)
  4. North-West (Top Left)
  5. South-West (Bottom Left)
  6. North-East (Top Right)
- Loot the Idol of Hakkar to complete [QC3447] (Secret of the Circle).
- Interact with the Altar of Hakkar to complete [QC3446] (Into the Depths).

Step: Upper Balconies & Troll Mini-Bosses:
- Climb to the upper levels and defeat the 6 Troll Mini-bosses to unlock the shield to Jammal'an:
  1. Gasher: Target [TAR5721 Gasher]
  2. Mijan: Target [TAR5720 Mijan]
  3. Zolo: Target [TAR5719 Zolo]
  4. Hukku: Target [TAR5722 Hukku]
  5. Zul'lor: Target [TAR5710 Zul'lor]
  6. Loro: Target [TAR5718 Loro]
- [A Warrior][A Paladin][A Shaman][A Warlock] Loot the Voodoo Feathers for your class quest.

Step: The Sanctum:
- Defeat Jammal'an the Prophet: Target [TAR5710 Jammal'an the Prophet] for [QC1446].
- Defeat the Green Drakes:
  - Defeat Morphaz: Target [TAR5709 Morphaz]
  - [A Rogue][A Hunter] Loot the Azure Key / Green Drake Tooth for your class quest.
  - Defeat Hazzas: Target [TAR5723 Hazzas]
- Defeat Shade of Eranikus: Target [TAR5709 Shade of Eranikus]
- Loot [CI10455,1 Essence of Eranikus] to accept [QA3373].
- Interact with the Essence Font in Eranikus' room to complete [QC3373].

Step: Summoning the God Hakkar (Optional):
- Go to the Altar of Hakkar in the main room.
- Use the Egg of Hakkar on the altar to begin the summoning event.
- Kill the Atal'ai Deathwasps to obtain blood to extinguish the four braziers.
- Defeat the Avatar of Hakkar: Target [TAR8443 Avatar of Hakkar] for [QC3528].

