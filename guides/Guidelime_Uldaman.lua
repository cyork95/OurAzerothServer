Defeat Archaedas (End Boss): Target [TAR2748 Archaedas]
Enter the Treasure Chamber behind Archaedas.
Loot the Platinum Discs: Access the discs for [QA2278] / [QA2279] / [QA2280] (The Platinum Discs) and [QC771] (The Chamber of Khaz'goroth).

